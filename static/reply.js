const text1 = document.querySelector('#text1');
const textInputBox1 = document.querySelector('#text-input-box1');

text1.addEventListener('click', function() {
  if (textInputBox1.style.display === 'block') {
    textInputBox1.style.display = 'none';
  } else {
    textInputBox1.style.display = 'block';
  }
});

